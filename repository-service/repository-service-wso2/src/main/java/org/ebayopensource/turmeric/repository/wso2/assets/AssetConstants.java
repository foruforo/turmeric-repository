package org.ebayopensource.turmeric.repository.wso2.assets;

/**
 * Constants used to store information into the WSO2 attributes/properties.
 * 
 * @author dcarver
 * 
 */
public class AssetConstants {

   /**
    * The owner of the asset.
    */
   public static final String TURMERIC_OWNER = "turmeric-owner";

   /**
    * The namespace for the asset
    */
   public static final String TURMERIC_NAMESPACE = "turmeric-namespace";

   /**
    * The version of the asset.
    */
   public static final String TURMERIC_VERSION = "turmeric-version";

   /**
    * A description of the asset
    */
   public static final String TURMERIC_DESCRIPTION = "turmeric-description";

   /**
    * A long description of the asset
    */
   public static final String TURMERIC_LONG_DESCRIPTION = "turmeric-long-description";

   /**
    * The name of the asset or artifact
    */
   public static final String TURMERIC_NAME = "turmeric-name";

   /**
    * The name of Display Name of the WSDL as defined by the Repository Service.
    */
   public static final String TURMERIC_DISPLAY_NAME = "turmeric-display-name";

   /**
    * The category of the WSDL as defined by the Repository Service.
    */
   public static final String TURMERIC_ARTIFACT_CATEGORY = "turmeric-artifact-category";

   /**
    * The type of the WSDL as defined by the Repository Service.
    */
   public static final String TURMERIC_TYPE = "turmeric-type";

   /**
    * Whether the asset is locked or not.
    */
   public static final String TURMERIC_LOCK = "turmeric-lock";
}
